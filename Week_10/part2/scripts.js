document.addEventListener("DOMContentLoaded", () => {
    showSemester(8); // You can change the semester number to show different semesters
});
function showSemester(semester) {
    const contentDiv = document.getElementById('content');
    const semesterData = syllabus[semester];
    
    if (semesterData) {
        let htmlContent = `<h2>${semesterData.title}</h2><ul>`;
        semesterData.subjects.forEach(subject => {
            htmlContent += `
                <li class="subject-title" onclick="toggleSubjectContent('${subject.name}')">
                    ${subject.name}
                    <span class="toggle-icon">+</span>
                </li>
                <div id="${subject.name}" class="subject-content">${subject.content}</div>
            `;
        });
        htmlContent += '</ul>';
        contentDiv.innerHTML = htmlContent;
    } else {
        contentDiv.innerHTML = '<p>Data not available for the selected semester.</p>';
    }

    // Smooth scrolling to content
    contentDiv.scrollIntoView({ behavior: 'smooth' });
}

function toggleSubjectContent(subjectName) {
    const content = document.getElementById(subjectName);
    const toggleIcon = content.previousElementSibling.querySelector('.toggle-icon');

    if (content.classList.contains("show")) {
        content.classList.remove("show");
        content.style.maxHeight = null;
        toggleIcon.textContent = "+";
    } else {
        // Collapse all other subject contents
        document.querySelectorAll('.subject-content').forEach(element => {
            element.classList.remove("show");
            element.style.maxHeight = null;
            element.previousElementSibling.querySelector('.toggle-icon').textContent = "+";
        });
        content.classList.add("show");
        content.style.maxHeight = content.scrollHeight + "px";
        toggleIcon.textContent = "-";
    }
}
